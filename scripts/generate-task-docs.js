#!/usr/bin/env node

/**
 * TaskMaster Documentation Generator
 * 
 * Generates MkDocs documentation from TaskMaster data
 * Integrates task status, progress, and project information
 */

const fs = require('fs');
const path = require('path');

// Configuration
const TASKMASTER_DIR = '.taskmaster';
const DOCS_DIR = 'docs';
const REPORTS_DIR = path.join(TASKMASTER_DIR, 'reports');
const TASK_STATUS_FILE = path.join(REPORTS_DIR, 'current-task-status.json');
const OUTPUT_FILE = path.join(DOCS_DIR, 'development', 'task-status.md');

/**
 * Load TaskMaster task status data
 */
function loadTaskStatus() {
  try {
    if (!fs.existsSync(TASK_STATUS_FILE)) {
      console.warn(`Task status file not found: ${TASK_STATUS_FILE}`);
      return null;
    }
    
    const data = fs.readFileSync(TASK_STATUS_FILE, 'utf8');
    return JSON.parse(data);
  } catch (error) {
    console.error('Error loading task status:', error.message);
    return null;
  }
}

/**
 * Generate task status documentation
 */
function generateTaskStatusDoc(taskData) {
  if (!taskData) {
    return generateFallbackDoc();
  }

  const { meta, taskStatus, summary } = taskData;
  
  let doc = `# Task Status Report

## Project Overview

**Generated:** ${new Date().toISOString()}  
**Total Tasks:** ${meta.totalTasks}  
**Completed Tasks:** ${meta.completedTasks}  
**In Progress:** ${meta.inProgressTasks}  
**Overall Progress:** ${meta.overallProgress}%

## Progress Summary

`;

  // Progress bar
  const progressBar = '█'.repeat(Math.floor(meta.overallProgress / 5)) + 
                     '░'.repeat(20 - Math.floor(meta.overallProgress / 5));
  doc += `\`\`\`
${progressBar} ${meta.overallProgress}%
\`\`\`

## Task Status

### High Priority Tasks
`;

  // High priority tasks
  const highPriorityTasks = taskStatus.filter(task => task.priority === 'high');
  highPriorityTasks.forEach(task => {
    const statusIcon = getStatusIcon(task.status);
    const progressBar = getProgressBar(task.completionPercentage);
    
    doc += `
#### ${statusIcon} Task ${task.taskId}: ${task.taskTitle}
- **Status:** ${task.status}
- **Progress:** ${progressBar} ${task.completionPercentage}%
- **Complexity:** ${task.complexityScore}/10
`;
  });

  doc += `
### Medium Priority Tasks
`;

  // Medium priority tasks
  const mediumPriorityTasks = taskStatus.filter(task => task.priority === 'medium');
  mediumPriorityTasks.forEach(task => {
    const statusIcon = getStatusIcon(task.status);
    const progressBar = getProgressBar(task.completionPercentage);
    
    doc += `
#### ${statusIcon} Task ${task.taskId}: ${task.taskTitle}
- **Status:** ${task.status}
- **Progress:** ${progressBar} ${task.completionPercentage}%
- **Complexity:** ${task.complexityScore}/10
`;
  });

  doc += `
### Low Priority Tasks
`;

  // Low priority tasks
  const lowPriorityTasks = taskStatus.filter(task => task.priority === 'low');
  lowPriorityTasks.forEach(task => {
    const statusIcon = getStatusIcon(task.status);
    const progressBar = getProgressBar(task.completionPercentage);
    
    doc += `
#### ${statusIcon} Task ${task.taskId}: ${task.taskTitle}
- **Status:** ${task.status}
- **Progress:** ${progressBar} ${task.completionPercentage}%
- **Complexity:** ${task.complexityScore}/10
`;
  });

  // Summary section
  if (summary) {
    doc += `
## Next Milestones

`;
    if (summary.nextMilestones) {
      summary.nextMilestones.forEach(milestone => {
        doc += `- ${milestone}\n`;
      });
    }

    doc += `
## Tasks Needing Attention

`;
    if (summary.tasksNeedingAttention) {
      summary.tasksNeedingAttention.forEach(taskId => {
        const task = taskStatus.find(t => t.taskId === taskId);
        if (task) {
          doc += `- **Task ${taskId}**: ${task.taskTitle} (${task.completionPercentage}%)\n`;
        }
      });
    }
  }

  doc += `
## Task Management

### TaskMaster Commands

\`\`\`bash
# View specific task
npm run task-master show <task-id>

# Update task status
npm run task-master set-status --id=<task-id> --status=<status>

# List all tasks
npm run task-master list

# Generate reports
npm run task-master report
\`\`\`

### Documentation Integration

This documentation is automatically generated from TaskMaster data and integrated with the MkDocs system. For more information, see:

- [Task Management Guide](task-management.md)
- [TaskMaster Integration](taskmaster-integration.md)
- [Development Workflows](task-workflows.md)

---

*Last updated: ${new Date().toISOString()}*
*Generated by: TaskMaster Documentation Generator*
`;

  return doc;
}

/**
 * Generate fallback documentation when TaskMaster data is unavailable
 */
function generateFallbackDoc() {
  return `# Task Status Report

## Status

TaskMaster data is currently unavailable. This may be due to:

- TaskMaster not being initialized
- Missing task status file
- Configuration issues

## Setup Instructions

To initialize TaskMaster and generate task documentation:

1. Install TaskMaster dependencies:
   \`\`\`bash
   npm install
   \`\`\`

2. Initialize TaskMaster:
   \`\`\`bash
   npm run task-master init
   \`\`\`

3. Generate task documentation:
   \`\`\`bash
   node scripts/generate-task-docs.js
   \`\`\`

## Manual Task Management

For manual task management, see:
- [Task Management Guide](task-management.md)
- [TaskMaster Guide](taskmaster-guide.md)

---

*Generated: ${new Date().toISOString()}*
`;
}

/**
 * Get status icon for task status
 */
function getStatusIcon(status) {
  const icons = {
    'done': '✅',
    'in-progress': '🔄',
    'pending': '⏳',
    'blocked': '🚫',
    'cancelled': '❌'
  };
  return icons[status] || '❓';
}

/**
 * Generate progress bar for completion percentage
 */
function getProgressBar(percentage) {
  const filled = Math.floor(percentage / 10);
  const empty = 10 - filled;
  return '█'.repeat(filled) + '░'.repeat(empty);
}

/**
 * Update MkDocs navigation to include task status
 */
function updateMkDocsNavigation() {
  const mkdocsPath = 'mkdocs.yml';
  
  try {
    if (!fs.existsSync(mkdocsPath)) {
      console.warn('mkdocs.yml not found, skipping navigation update');
      return;
    }

    let content = fs.readFileSync(mkdocsPath, 'utf8');
    
    // Check if task status is already in navigation
    if (content.includes('Task Status: development/task-status.md')) {
      console.log('Task status already in navigation');
      return;
    }

    // Add task status to development section
    const developmentSection = /(\s+- Development:\s*\n(?:\s+- .+\n)*)/;
    const match = content.match(developmentSection);
    
    if (match) {
      const newSection = match[1] + '      - Task Status: development/task-status.md\n';
      content = content.replace(developmentSection, newSection);
      
      fs.writeFileSync(mkdocsPath, content);
      console.log('Updated MkDocs navigation with task status');
    }
  } catch (error) {
    console.error('Error updating navigation:', error.message);
  }
}

/**
 * Main execution
 */
function main() {
  console.log('Generating TaskMaster documentation...');
  
  // Ensure output directory exists
  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }
  
  // Load task data and generate documentation
  const taskData = loadTaskStatus();
  const documentation = generateTaskStatusDoc(taskData);
  
  // Write documentation file
  fs.writeFileSync(OUTPUT_FILE, documentation);
  console.log(`Generated task documentation: ${OUTPUT_FILE}`);
  
  // Update navigation
  updateMkDocsNavigation();
  
  console.log('TaskMaster documentation generation complete!');
}

// Run if called directly
if (require.main === module) {
  main();
}

module.exports = {
  generateTaskStatusDoc,
  loadTaskStatus,
  updateMkDocsNavigation
};
